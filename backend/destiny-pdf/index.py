def handler(event, context):
    """PDF-отчёт «Полная карта судьбы». action: generate"""
    if event.get("httpMethod") == "OPTIONS":
        return {"statusCode": 200, "headers": CORS, "body": ""}

    body = {}
    if event.get("body"):
        body = json.loads(event["body"])

    action = body.get("action", "")
    token = event.get("headers", {}).get("X-Auth-Token", "")

    if action != "generate":
        return {"statusCode": 400, "headers": CORS, "body": json.dumps({"error": "Неизвестное действие"})}

    if not token:
        return {"statusCode": 401, "headers": CORS, "body": json.dumps({"error": "Не авторизован"})}

    conn = get_conn()
    user = get_user_by_token(conn, token)
    if not user:
        conn.close()
        return {"statusCode": 401, "headers": CORS, "body": json.dumps({"error": "Сессия истекла"})}

    birth_date = body.get("birth_date", "")
    if not birth_date:
        conn.close()
        return {"statusCode": 400, "headers": CORS, "body": json.dumps({"error": "Не указана дата рождения"})}

    if not check_purchase(conn, user["id"], birth_date):
        conn.close()
        return {"statusCode": 403, "headers": CORS, "body": json.dumps({"error": "Покупка не найдена"})}

    conn.close()

    pdf_bytes = build_pdf(birth_date)
    pdf_b64 = base64.b64encode(pdf_bytes).decode()
    filename = f"destiny_map_{birth_date}.pdf"

    return {
        "statusCode": 200,
        "headers": CORS,
        "body": json.dumps({"pdf": pdf_b64, "filename": filename}),
    }
