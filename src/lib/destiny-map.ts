export function calcDestinyMap(date: string): DestinyMapProfile | null {
  const lp = calcLifePath(date);
  if (!lp) return null;

  const { intermediate } = calcLifePathWithIntermediate(date);
  const matrix = calcPythagorasMatrix(date);

  const energyIndex = calcEnergyIndex(matrix);
  const mindIndex = calcMindIndex(matrix);
  const emotionIndex = calcEmotionIndex(matrix);
  const leaderIndex = calcLeaderIndex(matrix);
  const moneyIndex = calcMoneyIndex(matrix);
  const loveIndex = calcLoveIndex(matrix);

  const wealthIndex = clamp(Math.round((moneyIndex + leaderIndex + energyIndex) / 3), 0, 100);
  const successIndex = clamp(Math.round((mindIndex + leaderIndex + energyIndex) / 3), 0, 100);
  const realizationIndex = clamp(Math.round((successIndex + moneyIndex + mindIndex) / 3), 0, 100);
  const futurePotential = clamp(Math.round((realizationIndex + energyIndex) / 2), 0, 100);

  const archetype = determineArchetype(leaderIndex, mindIndex, emotionIndex, matrix, lp);
  const thinkingStyle = determineThinkingStyle(mindIndex, emotionIndex, matrix);
  const careers = calcCareers(matrix);
  const lifeCycles = calcLifeCycles(date);
  const keyAges = calcKeyAges(lp);
  const karmicLessons = calcKarmicLessons(matrix);
  const risks = calcRisks(leaderIndex, mindIndex, energyIndex, emotionIndex, moneyIndex);
  const bestPartners = determineBestPartners(archetype);

  const lpKey = lp > 22 ? reduce(lp) : lp;
  const purpose = PURPOSE_BY_LP[lpKey] || PURPOSE_BY_LP[reduce(lp)];
  const relationships = RELATIONSHIP_BY_LP[lpKey] || RELATIONSHIP_BY_LP[reduce(lp)];
  const strategy = STRATEGY_BY_TYPE[archetype.type];

  const strengths = STRENGTHS_DB[lpKey] || STRENGTHS_DB[reduce(lp)] || [];
  const weaknesses = WEAKNESSES_DB[lpKey] || WEAKNESSES_DB[reduce(lp)] || [];
  const characterTraits = CHAR_TRAITS[lpKey] || CHAR_TRAITS[reduce(lp)] || [];
  const behaviorPatterns = BEHAVIOR_PATTERNS[lpKey] || BEHAVIOR_PATTERNS[reduce(lp)] || [];

  return {
    lifePath: lp,
    lifePathBeforeReduce: intermediate,
    matrix,
    energyIndex,
    energyLevel: getEnergyLevel(energyIndex),
    mindIndex,
    emotionIndex,
    leaderIndex,
    moneyIndex,
    wealthIndex,
    loveIndex,
    successIndex,
    realizationIndex,
    futurePotential,
    archetype,
    thinkingStyle,
    strengths,
    weaknesses,
    careers,
    purpose: purpose?.main || "",
    purposeDetails: purpose?.details || [],
    relationships: relationships || "",
    bestPartners,
    lifeCycles,
    keyAges,
    karmicLessons,
    risks,
    strategy,
    characterTraits,
    behaviorPatterns,
  };
}
