function clamp(v: number, min: number, max: number): number {
  return Math.max(min, Math.min(max, Math.round(v)));
}

function sumAllDigits(date: string): number {
  return date.replace(/-/g, "").split("").map(Number).reduce((a, b) => a + b, 0);
}

function reduceToSingle(n: number): number {
  while (n > 9 && n !== 11 && n !== 22) {
    n = String(n).split("").reduce((a, d) => a + parseInt(d), 0);
  }
  return n;
}

function calcLifePathWithIntermediate(date: string): { final: number; intermediate: number } {
  const digits = date.replace(/-/g, "").split("").map(Number);
  const sum = digits.reduce((a, b) => a + b, 0);
  return { final: reduce(sum), intermediate: sum };
}

