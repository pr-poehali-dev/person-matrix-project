export function reduce(n: number): number {
  if (n === 11 || n === 22) return n;
  if (n <= 9) return n;
  const sum = String(n).split("").reduce((a, d) => a + parseInt(d), 0);
  return reduce(sum);
}

export function calcLifePath(date: string): number | null {
  if (!date) return null;
  const [y, m, d] = date.split("-").map(Number);
  if (!y || !m || !d) return null;
  const digits = [...String(d), ...String(m), ...String(y)].map(Number);
  const sum = digits.reduce((a, b) => a + b, 0);
  return reduce(sum);
}

export function calcCharacter(date: string): number | null {
  if (!date) return null;
  const day = parseInt(date.split("-")[2]);
  return reduce(day);
}

export function calcDestiny(date: string): number | null {
  if (!date) return null;
  const [y, m, d] = date.split("-").map(Number);
  const sum = reduce(d) + reduce(m) + reduce(y);
  return reduce(sum);
}

export function calcSoulUrge(date: string): number | null {
  if (!date) return null;
  const m = parseInt(date.split("-")[1]);
  return reduce(m);
}

export function calcMaturity(date: string): number | null {
  const lp = calcLifePath(date);
  const ch = calcCharacter(date);
  if (!lp || !ch) return null;
  return reduce(lp + ch);
}

export function calcPersonalYear(date: string): number | null {
  if (!date) return null;
  const [, m, d] = date.split("-").map(Number);
  if (!m || !d) return null;
  const currentYear = new Date().getFullYear();
  const digits = [...String(d), ...String(m), ...String(currentYear)].map(Number);
  const sum = digits.reduce((a, b) => a + b, 0);
  return reduce(sum);
}

