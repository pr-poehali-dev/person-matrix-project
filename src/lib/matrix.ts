export function reduce(n: number): number {
  if (n === 11 || n === 22) return n;
  if (n <= 9) return n;
  const sum = String(n).split("").reduce((a, d) => a + parseInt(d), 0);
  return reduce(sum);
}

export function calcLifePath(date: string): number | null {
  if (!date) return null;
  const [y, m, d] = date.split("-").map(Number);
  if (!y || !m || !d) return null;
  const digits = [...String(d), ...String(m), ...String(y)].map(Number);
  const sum = digits.reduce((a, b) => a + b, 0);
  return reduce(sum);
}

export function calcCharacter(date: string): number | null {
  if (!date) return null;
  const day = parseInt(date.split("-")[2]);
  return reduce(day);
}

export function calcDestiny(date: string): number | null {
  if (!date) return null;
  const [y, m, d] = date.split("-").map(Number);
  const sum = reduce(d) + reduce(m) + reduce(y);
  return reduce(sum);
}
