function getCompatLevel(a: number, b: number): string {
  const ra = a > 9 ? reduce(a) : a;
  const rb = b > 9 ? reduce(b) : b;
  const key = ra <= rb ? `${ra}-${rb}` : `${rb}-${ra}`;
  return COMPAT_MAP[key] || "neutral";
}

function compatLevelToScore(level: string): number {
  const map: Record<string, number> = { perfect: 5, good: 4, neutral: 3, challenging: 2, difficult: 1 };
  return map[level] || 3;
}

