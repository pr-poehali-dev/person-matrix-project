function getPairArchetype(lp1: number, lp2: number): string {
  const a = lp1 > 9 ? reduce(lp1) : lp1;
  const b = lp2 > 9 ? reduce(lp2) : lp2;
  const key = a <= b ? `${a}-${b}` : `${b}-${a}`;
  return PAIR_ARCHETYPES[key] || "Уникальный союз";
}

